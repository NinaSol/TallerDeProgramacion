package tp3.ejercicio8;

public class Faltante {
	private String nombre;
	private double cantidad;

	public Faltante(String nombre,double cantidad) {
		this.nombre = nombre;
		this.cantidad = cantidad;
	}

	@Override
	public String toString() {
		return "Faltante [nombre=" + nombre + ", cantidad=" + cantidad + "]";
	}
	
	
	
	
	
}
