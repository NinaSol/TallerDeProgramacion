package tp3.ejercicio9;

public class Candidato {
	private String nombre;
	private String email;
	
	
	public Candidato(String nombre, String email) {
		setNombre(nombre);
		setEmail(email);
	}


	private void setNombre(String nombre) {
		this.nombre = nombre;
	}


	private void setEmail(String email) {
		this.email = email;
	}
	
	
	
	
	
	
}
